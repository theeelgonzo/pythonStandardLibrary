# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA))
amountOfSalt = 1.4
print(round(amountOfSalt))

apple = -1.2
print(round(apple))
google = -1.6
print(round(google))

# abs()
distanceAway = -13
print(abs(distanceAway))
lengthOfRoot = -2.5
print(abs(lengthOfRoot))

# pow()
chanceOfTails = 0.5
inARowTail = 3
print(pow(chanceOfTails, inARowTail))
